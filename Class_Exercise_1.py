n1 = input("Enter first number:")
n2 = input("Enter second number:")

#Subtraction 
s = float(n1) - float(n2)
print("The result of the subtraction is: ", s)


#Addition
a = float(n1) + float(n2)
print("The result of the addition is: ", a)

#Multiplication
m = float(n1) * float(n2)
print("The result of the multiplication is: ", m)

#Power
p = float(n1) ** float(n2)
print("The result of the power operation is: ", p)

#Division
d = float(n1) / float(n2)
print("The result of the division is: ", d)
